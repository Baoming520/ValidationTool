    /// <summary>
    /// Class of extension rule for Common.Core.4038
    /// </summary>
    public abstract class CommonCore4038 : ExtensionRule
    {
        /// <summary>
        /// Gets Category property
        /// </summary>
        public override string Category
        {
            get
            {
                return "core";
            }
        }

        /// <summary>
        /// Gets rule name
        /// </summary>
