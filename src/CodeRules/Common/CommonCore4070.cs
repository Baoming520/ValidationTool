            }
        }

        /// <summary>
        /// Gets the IsOfflineContext property to which the rule applies.
        /// </summary>
        public override bool? IsOfflineContext
        {
            get
            {
                return false;
            }
        }

        /// <summary>
        /// Verify Common.Core.4070
        /// </summary>
        /// <param name="context">Service context</param>
        /// <param name="info">out parameter to return violation information when rule fail</param>
        /// <returns>true if rule passes; false otherwise</returns>
        public override bool? Verify(ServiceContext context, out ExtensionRuleViolationInfo info)
        {
            if (context == null)
            {
                throw new ArgumentNullException("context");
            }

            bool? passed = null;
            info = null;

            Response minResponse = WebHelper.Get(context.Destination, Constants.V4AcceptHeaderJsonMinimalMetadata, RuleEngineSetting.Instance().DefaultMaximumPayloadSize, context.RequestHeaders);
            Response fullResponse = WebHelper.Get(context.Destination, Constants.V4AcceptHeaderJsonFullMetadata, RuleEngineSetting.Instance().DefaultMaximumPayloadSize, context.RequestHeaders); 

            // Whether odata.metadata=full exist in response header.
            if (minResponse != null && !string.IsNullOrEmpty(minResponse.ResponseHeaders)
                && fullResponse != null && !string.IsNullOrEmpty(fullResponse.ResponseHeaders))              
            {
                if (minResponse.ResponseHeaders.Contains("odata.metadata=full") && fullResponse.ResponseHeaders.Contains("odata.metadata=full"))
                {
                    passed = true;
                }
                else
                {
                    passed = false;
                    info = new ExtensionRuleViolationInfo(this.ErrorMessage, context.Destination, context.ResponsePayload);
                }
            }            

            return passed;
        }
    }
