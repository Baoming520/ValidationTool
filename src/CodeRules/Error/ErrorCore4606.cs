        /// <summary>
        /// Verifies the extension rule.
        /// </summary>
        /// <param name="context">The Interop service context</param>
        /// <param name="info">out parameter to return violation information when rule does not pass</param>
        /// <returns>true if rule passes; false otherwise</returns>
        public override bool? Verify(ServiceContext context, out ExtensionRuleViolationInfo info)
        {
            if (context == null)
            {
                throw new ArgumentNullException("context");
            }

            bool? passed = null;
            info = null;

            XmlDocument xmlDoc = new XmlDocument();
            xmlDoc.LoadXml(context.ResponsePayload);
       //     xmlDoc.LoadXml("<error xmlns=\"http://docs.oasis-open.org/odata/ns/metadata\"><m:annotation></m:annotation><code>501</code><m:annotation target=\"code\"></m:annotation><message>Unsupported functionality</message><m:annotation target=\"message\"></m:annotation><target>query</target><details><detail><code>301</code><message>$search query option not supported</message><target>$search</target></detail></details><m:annotation target=\"innererror\"></m:annotation><m:annotation target=\"test\"></m:annotation></error>");
            XmlNode errorNode = xmlDoc.SelectSingleNode(@"//metadata:error", ODataNamespaceManager.Instance);

            if (null != errorNode)
            {
                XmlNode annotationNode = xmlDoc.SelectSingleNode(@"//metadata:error/metadata:annotation", ODataNamespaceManager.Instance);
                if (null != annotationNode)
                {
                    passed = true;
                }
                else
                {
                    passed = false;
                    info = new ExtensionRuleViolationInfo(this.ErrorMessage, context.Destination, context.ResponsePayload);
                }
            }

            return passed;
        }
